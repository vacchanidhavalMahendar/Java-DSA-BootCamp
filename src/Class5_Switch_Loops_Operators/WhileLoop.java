package Class5_Switch_Loops_Operators;

import java.util.Scanner;

public class WhileLoop {
    public static void main(String[] args) {
        Scanner sc= new Scanner(System.in);
        int n= sc.nextInt();
        //initialization
        int k=0;
        while(k< n){
            k++;
            System.out.println("Iam in while loop");

        }
    }
}
