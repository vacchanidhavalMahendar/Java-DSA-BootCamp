package Class5_Switch_Loops_Operators;


//SWITCH STATEMENT

public class SwitchDemo {
    public static void main(String[] args) {

        int x=10;
        switch(x){
            case 10:
                System.out.println("Gaurav ney chai pe lee");
            break; //break for breaking it
            case 15:
                System.out.println("Gaurav ney chai pe li and paan lei liya");
                break;
            default:
                System.out.println("Gaurav is Going for Home");
        }
    }
}
